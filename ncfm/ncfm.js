function localText(path) {
  return getByPath(mergedDictionary(), path) ?? getByPath(fallbackCopy, path) ?? path;
}

function mergedDictionary() {
  const language = currentLanguage();
  return deepMerge(fallbackCopy, dictionaries[language] || {});
}

function deepMerge(base, extra) {
  if (!extra || typeof extra !== "object" || Array.isArray(extra)) return extra ?? base;
  const output = { ...base };
  Object.entries(extra).forEach(([key, value]) => {
    if (value && typeof value === "object" && !Array.isArray(value)) output[key] = deepMerge(base?.[key] || {}, value);
    else output[key] = value;
  });
  return output;
}

function getByPath(source, path) {
  return path.split(".").reduce((value, part) => (value && Object.hasOwn(value, part) ? value[part] : undefined), source);
}

