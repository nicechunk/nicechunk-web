function normalizeVoxModel(model, filename) {
  const maxSize = Math.max(model.size.x, model.size.y, model.size.z, 1);
  const scale = Math.max(1, Math.floor(38 / maxSize));
  const centerX = (model.size.x - 1) / 2;
  const centerDepth = (model.size.y - 1) / 2;
  const centerY = (model.size.z - 1) / 2;
  const normalized = [];
  model.voxels.forEach((voxel) => {
    const baseX = Math.round((voxel.x - centerX) * scale);
    const baseY = Math.round((voxel.z - centerY) * scale);
    const baseZ = Math.round((centerDepth - voxel.y) * scale);
    for (let dx = 0; dx < scale; dx += 1) {
      for (let dy = 0; dy < scale; dy += 1) {
        for (let dz = 0; dz < scale; dz += 1) {
          normalized.push({
            x: baseX + dx,
            y: baseY + dy,
            z: baseZ + dz,
            color: hexToRgb(voxel.color || "#ffffff"),
            material: "vox",
          });
        }
      }
    }
  });
  return {
    filename,
    voxels: normalized,
    pathPoints: buildSilhouettePath(normalized),
  };
}

function buildSilhouettePath(sourceVoxels) {
  const cells = new Set(sourceVoxels.map((voxel) => `${voxel.x},${voxel.y}`));
  const edge = [];
  cells.forEach((key) => {
    const [x, y] = key.split(",").map(Number);
    if (!cells.has(`${x + 1},${y}`) || !cells.has(`${x - 1},${y}`) || !cells.has(`${x},${y + 1}`) || !cells.has(`${x},${y - 1}`)) {
      edge.push([x, y]);
    }
  });
  if (edge.length < 6) return pathPoints.length ? pathPoints : [[-10, -10], [10, -10], [10, 10], [-10, 10], [-10, -10]];
  const center = edge.reduce((sum, point) => [sum[0] + point[0] / edge.length, sum[1] + point[1] / edge.length], [0, 0]);
  return edge
    .sort((a, b) => Math.atan2(a[1] - center[1], a[0] - center[0]) - Math.atan2(b[1] - center[1], b[0] - center[0]))
    .filter((_, index) => index % Math.max(1, Math.floor(edge.length / 180)) === 0);
}

function prepareFourier() {
  importedModel = null;
  const outline = [
    [-15, 8], [-9, 11], [-3, 10], [1, 8], [10, 12], [16, 10], [12, 6], [4, 5],
    [2, 3], [2, -25], [-2, -25], [-2, 3], [-7, 4], [-14, 5], [-17, 7], [-15, 8],
  ];
  pathPoints = samplePolyline(outline, 220);
  coefficients = computeDft(pathPoints);
}

function samplePolyline(points, count) {
  const segments = [];
  let total = 0;
  for (let index = 0; index < points.length - 1; index += 1) {
    const a = points[index];
    const b = points[index + 1];
    const length = Math.hypot(b[0] - a[0], b[1] - a[1]);
    segments.push({ a, b, length });
    total += length;
  }
  const samples = [];
  for (let i = 0; i < count; i += 1) {
    let cursor = (i / count) * total;
    const segment = segments.find((item) => {
      if (cursor <= item.length) return true;
      cursor -= item.length;
      return false;
    }) || segments[segments.length - 1];
    const t = segment.length ? cursor / segment.length : 0;
    samples.push([
      segment.a[0] + (segment.b[0] - segment.a[0]) * t,
      segment.a[1] + (segment.b[1] - segment.a[1]) * t,
    ]);
  }
  return samples;
}

function computeDft(points) {
  const count = points.length;
  const result = [];
  for (let k = -34; k <= 34; k += 1) {
    let re = 0;
    let im = 0;
    for (let n = 0; n < count; n += 1) {
      const angle = (-2 * Math.PI * k * n) / count;
      const x = points[n][0];
      const y = points[n][1];
      re += x * Math.cos(angle) - y * Math.sin(angle);
      im += x * Math.sin(angle) + y * Math.cos(angle);
    }
    result.push({ k, re: re / count, im: im / count, amp: Math.hypot(re, im) / count });
  }
  return result.sort((a, b) => b.amp - a.amp);
}

function evalFourier(t, terms) {
  let x = 0;
  let y = 0;
  coefficients.slice(0, terms).forEach((coefficient) => {
    const angle = 2 * Math.PI * coefficient.k * t;
    x += coefficient.re * Math.cos(angle) - coefficient.im * Math.sin(angle);
    y += coefficient.re * Math.sin(angle) + coefficient.im * Math.cos(angle);
  });
  return [x, y];
}

function rebuildVoxels() {
  if (importedModel) {
    voxels = importedModel.voxels.slice();
    voxelCount.textContent = String(voxels.length);
    updateBadges();
    return;
  }
  const depth = Number(depthRange.value);
  voxels = [];
  for (let y = -25; y <= 13; y += 1) {
    for (let x = -18; x <= 18; x += 1) {
      const xy = fxy(x, y);
      if (!xy.inside) continue;
      const localDepth = Math.max(1, Math.round(depth / 2 + Math.sin(x * 0.27 + y * 0.31) * 0.7));
      for (let z = -localDepth; z <= localDepth; z += 1) {
        if (fz(z, localDepth)) voxels.push({ x, y, z, color: xy.color, material: xy.material });
      }
    }
  }
  voxels.sort((a, b) => (a.x + a.y + a.z) - (b.x + b.y + b.z));
  voxelCount.textContent = String(voxels.length);
  updateBadges();
}

function fxy(x, y) {
  const handle = Math.abs(x) <= 2 && y >= -25 && y <= 4;
  const headCore = y >= 4 && y <= 11 && Math.abs(x) <= 17 - Math.abs(y - 8) * 1.5;
  const pickTip = (x > 9 && y >= 5 && y <= 10 && x + (y - 7) * 0.6 <= 17) || (x < -9 && y >= 5 && y <= 10 && -x - (y - 7) * 0.6 <= 17);
  const neck = Math.abs(x) <= 6 && y >= 2 && y <= 7;
  const inside = handle || headCore || pickTip || neck;
  const metal = y >= 4 || Math.abs(x) > 3;
  const shade = Math.sin(x * 0.67 + y * 0.29) * 10;
  return {
    inside,
    material: metal ? "metal" : "wood",
    color: metal ? rgb(154 + shade, 196 + shade, 214 + shade) : rgb(151 + shade, 96 + shade * 0.5, 50),
  };
}

function fz(z, localDepth) {
  return Math.cos((Math.PI * z) / Math.max(1, localDepth + 0.6)) > -0.4;
}

function rgb(r, g, b) {
  return `rgb(${Math.round(r)}, ${Math.round(g)}, ${Math.round(b)})`;
}

function hexToRgb(hex) {
  const value = String(hex || "#ffffff").replace("#", "");
  const numeric = Number.parseInt(value.length === 3 ? value.replace(/(.)/g, "$1$1") : value.slice(0, 6), 16);
  if (!Number.isFinite(numeric)) return "rgb(255, 255, 255)";
  return rgb((numeric >> 16) & 255, (numeric >> 8) & 255, numeric & 255);
}

